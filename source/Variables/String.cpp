#include "String.h"

using namespace std;


/****************************************************************************************
 *
 ****************************************************************************************/
String::String(Visibility visibility, bool isStatic, string &value)
    : Variable(visibility, isStatic)
{
    if (value[0] == '"' || value[0] == '\'') {
        this->value = value.substr(1, value.length() - 2);
    } else {
        this->value = value;
    }
}


/****************************************************************************************
 *
 ****************************************************************************************/
String::~String() {
    //
}


/****************************************************************************************
 *
 ****************************************************************************************/
string String::getStringValue() {
    return this->value;
}
