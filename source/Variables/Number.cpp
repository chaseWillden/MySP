#include "Number.h"


using namespace std;


/****************************************************************************************
 *
 ****************************************************************************************/
Number::Number(Visibility visibility, bool isStatic, int value)
    : Variable(visibility, isStatic)
{
    this->value = value;
}


/****************************************************************************************
 *
 ****************************************************************************************/
Number::~Number() {
    //
}


/****************************************************************************************
 *
 ****************************************************************************************/
char Number::getType() {
    return 'n';
}


/****************************************************************************************
 *
 ****************************************************************************************/
string Number::getTypeString() {
    return "number";
}


/****************************************************************************************
 *
 ****************************************************************************************/
float Number::getNumberValue() {
    return this->value;
}
